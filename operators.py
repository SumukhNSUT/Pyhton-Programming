# ARITHEMATIC OPERATORS
print(5 + 2)
print(5 - 2)
print(5 * 2)
print(5 / 2)
print(5 % 2)
# some additional
print(5 // 2)  # just for getting 2 of 2.5 (integer part only)
print(5 ** 2)  # 5 raised to power 2

# COMPARISON OPERATORS
print(3 > 2)
print(3 < 2)
print(3 <= 2)
print(5 >= 5)
print(3 == 3)
print(4 == 5)
print(3 != 2)
print(2 > 3 or 3 > 2)  # true if any one is true
print(2 > 3 and 3 > 4)  # true if both are true
print(not 2 > 3)  # negation of output
